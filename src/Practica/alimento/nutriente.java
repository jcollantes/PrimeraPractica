package Practica.alimento;

public class nutriente {

    private String nomNutriente;
    private String unidadMedia;
    private String estado;

    public String getNomNutriente() {
        return nomNutriente;
    }

    public void setNomNutriente(String nomNutriente) {
        this.nomNutriente = nomNutriente;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getUnidadMedia() {
        return unidadMedia;
    }

    public void setUnidadMedia(String unidadMedia) {
        this.unidadMedia = unidadMedia;
    }
}
